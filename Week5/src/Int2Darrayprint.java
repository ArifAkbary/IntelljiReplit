public class Int2Darrayprint {
    public static void main(String[] args) {
        double [][] x={
                {2.8,4.0,6.6,4.0},
                {8.0,3.0,12.2,2.0},
                {2.4,6.2,8.0,3.2}
        };
        for(double[]num:x){
            for(double z:num){
                System.out.print(z+" ");
            }
        }






    }
}
