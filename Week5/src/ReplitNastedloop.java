public class ReplitNastedloop {

    public static void main(String []args){

        for(int i=1; i<=10; i++){
            System.out.print(i+" ");

        }
        System.out.println();

        for(int j=2; j<=20; j++){
            if(j%2==0)
                System.out.print(j+" ");

        }
        System.out.println();
        for(int k=3;k<=30; k++){
            if(k%3==0)
                System.out.print(k+" ");
        }
        System.out.println();

        for(int l=4;l<=40; l++){
            if(l%4==0)
                System.out.print(l+" ");
        }
        System.out.println();

        for(int k=5; k<=50; k++){
            if(k%5==0)
                System.out.print(k+" ");
        }
    }
}










